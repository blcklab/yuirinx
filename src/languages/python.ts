/** Python grammar with triple-quoted string handling. */
export const python: Grammar = {
  id: "python",
  aliases: ["py"],
  states: {
    root: {
      rules: [
        { type: "comment", pattern: /#[^\r\n]*/ },
        {
          type: "string",
          pattern: /(?:[rubf]{0,2})(?:"""|''')/i,
          push: (match) =>
            match[0].endsWith('"""') ? "tripleDouble" : "tripleSingle",
        },
        {
          type: "string",
          pattern: /(?:[rubf]{0,2})"(?:\\[\s\S]|[^"\\\r\n])*"?/i,
        },
        {
          type: "string",
          pattern: /(?:[rubf]{0,2})'(?:\\[\s\S]|[^'\\\r\n])*'?/i,
        },
        { type: "decorator", pattern: /@[A-Za-z_]\w*(?:\.[A-Za-z_]\w*)*/ },
        numberRule,
        { type: "boolean", pattern: /\b(?:True|False)\b/ },
        { type: "null", pattern: /\bNone\b/ },
        { type: "keyword", pattern: words(KEYWORDS) },
        { type: "builtin", pattern: words(BUILTINS) },
        { type: "function.call", pattern: /\b[A-Za-z_]\w*(?=\s*\()/ },
        { type: "type", pattern: /\b[A-Z][A-Za-z\d_]*\b/ },
        { type: "variable", pattern: /\b[A-Za-z_]\w*\b/ },
        operatorRule,
        punctuationRule,
        whitespaceRule,
      ],
    },
    tripleDouble: {
      fallbackType: "string",
      rules: [
        { type: "string", pattern: /"""/, pop: true },
        { type: "string.escape", pattern: /\\[\s\S]/ },
        { type: "string", pattern: /[^"\\]+/ },
        { type: "string", pattern: /"|\\/ },
      ],
    },
    tripleSingle: {
      fallbackType: "string",
      rules: [
        { type: "string", pattern: /'''/, pop: true },
        { type: "string.escape", pattern: /\\[\s\S]/ },
        { type: "string", pattern: /[^'\\]+/ },
        { type: "string", pattern: /'|\\/ },
      ],
    },
  },
};
