/** Ruby grammar. */
export const ruby: Grammar = {
  id: "ruby",
  aliases: ["rb"],
  states: {
    root: {
      rules: [
        { type: "comment", pattern: /#[^\r\n]*/ },
        { type: "comment.documentation", pattern: /=begin[\s\S]*?(?:=end|$)/ },
        { type: "string", pattern: /"(?:\\[\s\S]|[^"\\\r\n])*"?/ },
        { type: "string", pattern: /'(?:\\[\s\S]|[^'\\\r\n])*'?/ },
        { type: "string", pattern: /%[qQwWiIxr]?[^\w\s](?:\\.|[^\r\n])*?/ },
        { type: "symbol", pattern: /:[A-Za-z_]\w*[!?=]?/ },
        { type: "variable", pattern: /(?:@@?|\$)[A-Za-z_]\w*/ },
        numberRule,
        { type: "keyword", pattern: words(KEYWORDS) },
        { type: "function.call", pattern: /\b[A-Za-z_]\w*[!?=]?(?=\s*\()/ },
        { type: "class", pattern: /\b[A-Z][A-Za-z\d_]*\b/ },
        { type: "variable", pattern: /\b[A-Za-z_]\w*[!?=]?\b/ },
        operatorRule,
        punctuationRule,
        whitespaceRule,
      ],
    },
  },
};
