/** SQL grammar with common dialect keywords and functions. */
export const sql: Grammar = {
  id: "sql",
  states: {
    root: {
      rules: [
        { type: "comment", pattern: /--[^\r\n]*/ },
        { type: "comment", pattern: /\/\*[\s\S]*?(?:\*\/|$)/ },
        singleStringRule,
        doubleStringRule,
        { type: "string", pattern: /`(?:``|[^`])*`?/ },
        numberRule,
        { type: "boolean", pattern: /\b(?:true|false)\b/i },
        { type: "null", pattern: /\bnull\b/i },
        { type: "keyword", pattern: words(KEYWORDS, "i") },
        { type: "function.call", pattern: /\b[A-Za-z_]\w*(?=\s*\()/ },
        { type: "property", pattern: /\b[A-Za-z_]\w*(?=\s*(?:,|\bfrom\b))/i },
        { type: "variable", pattern: /(?:@{1,2}|:)[A-Za-z_]\w*/ },
        { type: "variable", pattern: /\b[A-Za-z_]\w*\b/ },
        operatorRule,
        punctuationRule,
        whitespaceRule,
      ],
    },
  },
};
